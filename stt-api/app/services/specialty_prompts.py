# ── 공통 프롬프트 접미사 (모든 진료과에 붙음) ──
_COMMON_SUFFIX = (
    " 네, 예, 아, 그, 음, 그러니까, 그래서, "
    "선생님, 원장님, 환자분, 어머님, 아버님, "
    "괜찮습니다, 감사합니다, 수고하세요"
)


def get_specialty_prompt(specialty: str | None = None, type_num: int | None = None) -> str:
    """진료과 또는 Type 번호에 맞는 Whisper 초기 프롬프트 반환.

    Args:
        specialty: 진료과 이름 (예: "정형외과", "안과")
        type_num: Type 번호 (1~16) — specialty가 없을 때 자동 매핑

    Returns:
        해당 진료과의 Whisper 초기 프롬프트 문자열
    """
    # 1) specialty가 직접 주어진 경우
    if specialty and specialty in SPECIALTY_PROMPTS:
        return SPECIALTY_PROMPTS[specialty] + _COMMON_SUFFIX

    # 2) type_num으로 자동 매핑
    if type_num is not None and type_num in TYPE_TO_SPECIALTY:
        mapped = TYPE_TO_SPECIALTY[type_num]
        return SPECIALTY_PROMPTS[mapped] + _COMMON_SUFFIX

    # 3) 진료과 이름이 부분 매칭되는 경우
    if specialty:
        for key in SPECIALTY_PROMPTS:
            if specialty in key or key in specialty:
                return SPECIALTY_PROMPTS[key] + _COMMON_SUFFIX

    # 4) 기본값: 모든 진료과 핵심 용어를 합친 범용 프롬프트
    return _build_universal_prompt()


